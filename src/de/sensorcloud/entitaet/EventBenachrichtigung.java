package de.sensorcloud.entitaet;

public class EventBenachrichtigung {

	private String eveBenID;
	private String eveBenEveID;
	private String eveBenWeg;

	
	public String getEveBenID() {
		return eveBenID;
	}

	public void setEveBenID(String eveBenID) {
		this.eveBenID = eveBenID;
	}

	public String getEveBenEveID() {
		return eveBenEveID;
	}

	public void setEveBenEveID(String eveBenEveID) {
		this.eveBenEveID = eveBenEveID;
	}

	public String getEveBenWeg() {
		return eveBenWeg;
	}

	public void setEveBenWeg(String eveBenWeg) {
		this.eveBenWeg = eveBenWeg;
	}

}
