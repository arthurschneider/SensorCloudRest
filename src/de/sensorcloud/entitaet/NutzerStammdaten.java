package de.sensorcloud.entitaet;

public class NutzerStammdaten {

	private String nutStaID;
	private String nutStaAdrID;
	private String nutStaAnr;
	private String nutStaNam;
	private String nutStaVor;
	private String nutStaFir;
	private String nutStaDatEin;

	
	public String getNutStaID() {
		return nutStaID;
	}

	public void setNutStaID(String nutStaID) {
		this.nutStaID = nutStaID;
	}

	public String getNutStaAdrID() {
		return nutStaAdrID;
	}

	public void setNutStaAdrID(String nutStaAdrID) {
		this.nutStaAdrID = nutStaAdrID;
	}

	public String getNutStaAnr() {
		return nutStaAnr;
	}

	public void setNutStaAnr(String nutStaAnr) {
		this.nutStaAnr = nutStaAnr;
	}

	public String getNutStaNam() {
		return nutStaNam;
	}

	public void setNutStaNam(String nutStaNam) {
		this.nutStaNam = nutStaNam;
	}

	public String getNutStaVor() {
		return nutStaVor;
	}

	public void setNutStaVor(String nutStaVor) {
		this.nutStaVor = nutStaVor;
	}

	public String getNutStaFir() {
		return nutStaFir;
	}

	public void setNutStaFir(String nutStaFir) {
		this.nutStaFir = nutStaFir;
	}

	public String getNutStaDatEin() {
		return nutStaDatEin;
	}

	public void setNutStaDatEin(String nutStaDatEin) {
		this.nutStaDatEin = nutStaDatEin;
	}

}
