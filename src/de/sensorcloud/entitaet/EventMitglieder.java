package de.sensorcloud.entitaet;

public class EventMitglieder {

	private String eveMitID;
	private String EveMitEveID;
	private String eveMitSenEveID;
	private String eveMitRei;

	
	public String getEveMitID() {
		return eveMitID;
	}

	public void setEveMitID(String eveMitID) {
		this.eveMitID = eveMitID;
	}

	public String getEveMitEveID() {
		return EveMitEveID;
	}

	public void setEveMitEveID(String eveMitEveID) {
		EveMitEveID = eveMitEveID;
	}

	public String getEveMitSenEveID() {
		return eveMitSenEveID;
	}

	public void setEveMitSenEveID(String eveMitSenEveID) {
		this.eveMitSenEveID = eveMitSenEveID;
	}

	public String getEveMitRei() {
		return eveMitRei;
	}

	public void setEveMitRei(String eveMitRei) {
		this.eveMitRei = eveMitRei;
	}

}
