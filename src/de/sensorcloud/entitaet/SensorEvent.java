package de.sensorcloud.entitaet;

public class SensorEvent {

	private String senEveID;
	private String senEveSenID;
	private String senEvePhyNam;
	private String senEveVop;
	private String senEveWer;

	public String getSenEveID() {
		return senEveID;
	}

	public void setSenEveID(String senEveID) {
		this.senEveID = senEveID;
	}

	public String getSenEveSenID() {
		return senEveSenID;
	}

	public void setSenEveSenID(String senEveSenID) {
		this.senEveSenID = senEveSenID;
	}

	public String getSenEvePhyNam() {
		return senEvePhyNam;
	}

	public void setSenEvePhyNam(String senEvePhyNam) {
		this.senEvePhyNam = senEvePhyNam;
	}

	public String getSenEveVop() {
		return senEveVop;
	}

	public void setSenEveVop(String senEveVop) {
		this.senEveVop = senEveVop;
	}

	public String getSenEveWer() {
		return senEveWer;
	}

	public void setSenEveWer(String senEveWer) {
		this.senEveWer = senEveWer;
	}

}
