package de.sensorcloud.entitaet;

public class NutzerSicherheit {

	private String NutSicID;
	private String NutSicNutStaID;
	private String NutSicPas;
	private String NutSicPriKey;
	private String NutSicPubKey;

	
	public String getNutSicID() {
		return NutSicID;
	}

	public void setNutSicID(String nutSicID) {
		NutSicID = nutSicID;
	}

	public String getNutSicNutStaID() {
		return NutSicNutStaID;
	}

	public void setNutSicNutStaID(String nutSicNutStaID) {
		NutSicNutStaID = nutSicNutStaID;
	}

	public String getNutSicPas() {
		return NutSicPas;
	}

	public void setNutSicPas(String nutSicPas) {
		NutSicPas = nutSicPas;
	}

	public String getNutSicPriKey() {
		return NutSicPriKey;
	}

	public void setNutSicPriKey(String nutSicPriKey) {
		NutSicPriKey = nutSicPriKey;
	}

	public String getNutSicPubKey() {
		return NutSicPubKey;
	}

	public void setNutSicPubKey(String nutSicPubKey) {
		NutSicPubKey = nutSicPubKey;
	}

}
