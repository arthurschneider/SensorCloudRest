package de.sensorcloud.entitaet;

public class ServiceMitTyp {

	private String serID;
	private String serBez;
	private String serTyp;
	

	public String getSerID() {
		return serID;
	}

	public void setSerID(String serID) {
		this.serID = serID;
	}

	public String getSerBez() {
		return serBez;
	}

	public void setSerBez(String serBez) {
		this.serBez = serBez;
	}

	public String getSerTyp() {
		return serTyp;
	}

	public void setSerTyp(String serTyp) {
		this.serTyp = serTyp;
	}

}
