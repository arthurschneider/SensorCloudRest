package de.sensorcloud.entitaet;

public class Mandanten {
	
	private String ManID;
	private String ManNutStaID;
	private String ManBez;

	
	public String getManID() {
		return ManID;
	}

	public void setManID(String manID) {
		ManID = manID;
	}

	public String getManNutStaID() {
		return ManNutStaID;
	}

	public void setManNutStaID(String manNutStaID) {
		ManNutStaID = manNutStaID;
	}

	public String getManBez() {
		return ManBez;
	}

	public void setManBez(String manBez) {
		ManBez = manBez;
	}

}
