package de.sensorcloud.entitaet;

public class Event {

	private String eveID;
	private String eveArt;
	private String eveBez;
	private String eveNac;
	private String eveTimSta;


	public String getEveID() {
		return eveID;
	}

	public void setEveID(String eveID) {
		this.eveID = eveID;
	}

	public String getEveArt() {
		return eveArt;
	}

	public void setEveArt(String eveArt) {
		this.eveArt = eveArt;
	}

	public String getEveBez() {
		return eveBez;
	}

	public void setEveBez(String eveBez) {
		this.eveBez = eveBez;
	}

	public String getEveNac() {
		return eveNac;
	}

	public void setEveNac(String eveNac) {
		this.eveNac = eveNac;
	}

	public String getEveTimSta() {
		return eveTimSta;
	}

	public void setEveTimSta(String eveTimSta) {
		this.eveTimSta = eveTimSta;
	}

}
