package de.sensorcloud.entitaet;

public class AktorEvent {

	private String aktEveID;
	private String aktEveAktID;
	private String aktEvePhyNam;
	private String aktEveVop;
	private String aktEveWer;

	
	public String getAktEveID() {
		return aktEveID;
	}

	public void setAktEveID(String aktEveID) {
		this.aktEveID = aktEveID;
	}

	public String getAktEveAktID() {
		return aktEveAktID;
	}

	public void setAktEveAktID(String aktEveAktID) {
		this.aktEveAktID = aktEveAktID;
	}

	public String getAktEvePhyNam() {
		return aktEvePhyNam;
	}

	public void setAktEvePhyNam(String aktEvePhyNam) {
		this.aktEvePhyNam = aktEvePhyNam;
	}

	public String getAktEveVop() {
		return aktEveVop;
	}

	public void setAktEveVop(String aktEveVop) {
		this.aktEveVop = aktEveVop;
	}

	public String getAktEveWer() {
		return aktEveWer;
	}

	public void setAktEveWer(String aktEveWer) {
		this.aktEveWer = aktEveWer;
	}

}
